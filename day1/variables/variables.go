package main

import "fmt"

func main() {
	var str = "string"
	otherString := "otherString"
	var defaultString string

	fmt.Println(str)
	fmt.Println(otherString)
	fmt.Println(defaultString)

	var integer = 5
	otherInteger := 10
	var defaultInteger int = 8

	fmt.Println(integer)
	fmt.Println(otherInteger)
	fmt.Println(defaultInteger)

	var boolean = false
	otherBoolean := true
	var defaultBoolean bool

	fmt.Println(boolean)
	fmt.Println(otherBoolean)
	fmt.Println(defaultBoolean)
}
